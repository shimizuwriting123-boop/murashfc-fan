import type { TournamentSlug } from "./players";

export interface TournamentFeature {
  title: string;
  body: string;
}

/**
 * 大会のスタッフ・コーチング体制。
 * playerSlug があれば /players/{slug} へリンク、なければテキスト表示（医師など名鑑外の人物）。
 */
export interface StaffRosterEntry {
  /** 役職名（例：「監督」「1stコーチ」「GKコーチ」「トレーナー」「医師」） */
  role: string;
  name: string;
  /** 表示後ろに付ける補足（例：「途中まで選手#8」「（GOD）」） */
  noteSuffix?: string;
  /** 選手名鑑にレコードがある場合の slug */
  playerSlug?: string;
}

export interface DetailItem {
  /** サブ見出し（省略時は本文のみのブロック） */
  title?: string;
  /** 段落の配列。`**強調**` 記法で強調表現が使える */
  body: string[];
  /** ※ で始まる補足メモ */
  note?: string;
  /** 「伝説」級のエピソードは赤枠で強調表示する */
  emphasized?: boolean;
}

export interface DetailSection {
  /** 省略時はアイコンなしの見出しとして描画 */
  icon?: string;
  title: string;
  items: DetailItem[];
}

export interface TournamentDetail {
  hostCountry: string;
  sections: DetailSection[];
}

export interface Tournament {
  /** 内部ID（players.ts の TournamentSlug と一致） */
  slug: TournamentSlug;
  /** URL スラッグ（/tournaments/{urlSlug}） */
  urlSlug: string;
  no: string;
  name: string;
  date: string;
  format: string;
  result: string;
  record?: string;
  champion?: string;
  /** 例：オファー制／トライアウト形式 */
  selectionMethod?: string;
  highlight: string;
  episodeTitle?: string;
  episodes?: string[];
  feature?: TournamentFeature;
  /** 個別ページのリッチ本文。未定義の大会は /tournaments/{urlSlug} を生成しない */
  detail?: TournamentDetail;
  /** その大会のコーチング・スタッフ体制 */
  staffRoster?: StaffRosterEntry[];
  /**
   * SQUAD セクションの直下に表示する注記。
   * 不参加選手・情報不明箇所などの TBD 情報を補足する用途。
   */
  squadNote?: string;
}

