import type { TournamentSlug } from "./players";

export interface TournamentFeature {
  title: string;
  body: string;
}

export interface DetailItem {
  /** サブ見出し（省略時は本文のみのブロック） */
  title?: string;
  /** 段落の配列。`**強調**` 記法で強調表現が使える */
  body: string[];
  /** ※ で始まる補足メモ */
  note?: string;
}

export interface DetailSection {
  icon: string;
  title: string;
  items: DetailItem[];
}

export interface TournamentDetail {
  hostCountry: string;
  sections: DetailSection[];
}

export interface Tournament {
  /** 内部ID（players.ts の TournamentSlug と一致） */
  slug: TournamentSlug;
  /** URL スラッグ（/tournaments/{urlSlug}） */
  urlSlug: string;
  no: string;
  name: string;
  date: string;
  format: string;
  result: string;
  record?: string;
  champion?: string;
  highlight: string;
  episodeTitle?: string;
  episodes?: string[];
  feature?: TournamentFeature;
  /** 個別ページのリッチ本文。未定義の大会は /tournaments/{urlSlug} を生成しない */
  detail?: TournamentDetail;
}

